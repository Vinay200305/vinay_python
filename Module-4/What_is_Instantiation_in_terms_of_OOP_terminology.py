#What is Instantiation in terms of OOP terminology?


'''

Class                > A user-defined prototype for an object that defines a set of attributes that characterize any object of the class. The attributes are data members and methods, accessed via dot notation.
Class variable       > A variable that is shared by all instances of a class. Class variables are defined within a class but outside any of the class's methods.
Data member          > A class variable or instance variable that holds data associated with a class and its objects.
Function overloading > The assignment of more than one behavior to a particular function. The operation performed varies by the types of objects or arguments involved.
Instance variable    > A variable that is defined inside a method and belongs only to the current instance of a class.
Inheritance          > The transfer of the characteristics of a class to other classes that are derived from it.
Instance             > An object created from a class. Each instance has its own set of attributes and methods.
Instantiation        >The creation of an instance of a class.
Object               > A unique instance of a data structure that's defined by its class.
Class Constructor    > A special method within a class, often named __init__(), that is called during instantiation. It initializes the object's attributes and performs any necessary setup.

'''
